/**
 * @author - Raja Malik
 * 
 * Margin is a type for defining margins which can be used with in
 * a Graph. Four margins can be declared as valid integers:
 * 
 * 1. top
 * 2. right
 * 3. bottom
 * 4. left  
 */

export interface Margin
{ 
    top: number, 
    right: number, 
    bottom: number, 
    left: number 
}
